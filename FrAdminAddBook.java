    public void connectDatabase(){
        try{
            String strURL = "jdbc:mysql://localhost:3306/ls?useLegacyDatetimeCode=false&serverTimezone=UTC";
            String strDatabaseUsername = "root";
            String strDatabasePassword = "";
            conMain = DriverManager.getConnection(strURL, strDatabaseUsername, strDatabasePassword);
        }   
        catch(SQLException error){
            System.out.println(error);
        }
    }
    
    public int incrementID(){
        String strID;
        int intID = 0;
        connectDatabase();
        String strquery = "SELECT book_ID FROM BookList ORDER BY book_ID";
        try{
            pstmtMain = conMain.prepareStatement(strquery);
            rsMain = pstmtMain.executeQuery();
            while(rsMain.next()){
                strID = rsMain.getString("book_ID");
                intID = Integer.parseInt(strID)+1;
            }
        }
        catch(SQLException error){
            System.out.println(error);
        }
        if(intID == 0){
            return 190000;
        }
        else{
            return intID;
        }
    }
