    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        connectDatabase();
        String strID = tfldID.getText();
        String strName = tfldName.getText();
        String strAuthor = tfldAuthor.getText();
        String strQuery = "INSERT INTO"
                + " BookList (book_ID, book_Name, book_Author)"
                + " values("+strID+", '"+strName+"', '"+strAuthor+"')";
        if(strName.equals("")||strAuthor.equals("")){
            JOptionPane.showMessageDialog(null, "Fields must not be Blank.");
        }
        else{
            try{
                pstmtMain = conMain.prepareStatement(strQuery);
                pstmtMain.execute();
                new FrAdminHomePage().setVisible(true);
                this.dispose();
            }
            catch(SQLException error){
                System.out.println(error);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

