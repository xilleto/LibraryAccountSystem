    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        connectDatabase();
        String strID = tfldID.getText();
        String strLName = tfldLName.getText();
        String strFName = tfldFName.getText();
        String strUsername = ""+strLName.charAt(0)+strFName.charAt(0)+strID.substring(3);
        String strPassword = pfldPassword.getText();
        String strQuery = "INSERT INTO"
                + " StudentList (student_ID, student_LName, student_FName, student_Username, student_Password)"
                + " values("+strID+", '"+strLName+"', '"+strFName+"', '"+strUsername+"', '"+strPassword+"')";
        if(strLName.equals("")||strFName.equals("")||strPassword.equals("")){
            JOptionPane.showMessageDialog(null, "Fields must not be Blank.");
        }
        else{
            try{
                pstmtMain = conMain.prepareStatement(strQuery);
                pstmtMain.execute();
                JOptionPane.showMessageDialog(null, "Successfully Registered with Username of ["+strUsername+"].", "REGISTERED", JOptionPane.INFORMATION_MESSAGE);
                new FrLogin().setVisible(true);
                this.dispose();
            }
            catch(SQLException error){
                System.out.println(error);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

