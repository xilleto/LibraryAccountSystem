    public int incrementID(){
        String strID;
        int intID = 0;
        connectDatabase();
        String strquery = "SELECT student_ID from StudentList order by student_ID";
        try{
            pstmtMain = conMain.prepareStatement(strquery);
            rsMain = pstmtMain.executeQuery();
            while(rsMain.next()){
                strID = rsMain.getString("student_ID");
                intID = Integer.parseInt(strID)+1;
            }
        }
        catch(SQLException error){
            System.out.println(error);
        }
        if(intID == 0){
            return 190000;
        }
        else{
            return intID;
        }
    }
