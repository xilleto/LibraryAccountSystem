    public void loadBookList(){
        connectDatabase();
        DefaultTableModel tblModel = (DefaultTableModel) tblList.getModel();
        tblModel.setRowCount(0);
        String strQuery = "SELECT book_ID, book_Name, book_Author FROM BookList ORDER BY book_ID";
        try{
            pstmtMain = conMain.prepareStatement(strQuery);
            rsMain = pstmtMain.executeQuery();
            while(rsMain.next()){
                String strID = rsMain.getString("book_ID");
                String strName = rsMain.getString("book_Name");
                String strAuthor = rsMain.getString("book_Author");
                tblModel.addRow(new Object[]{strID, strName, strAuthor});
            }
        }
        catch(SQLException error){
            System.out.println(error);
        }
    }
