    public void createDatabase(){
        String strURL = "jdbc:mysql://localhost:3306/ls?useLegacyDatetimeCode=false&serverTimezone=UTC&createDatabaseIfNotExist=true";
        String strQueryBookList = "CREATE TABLE `BookList`("
                + "`book_ID` INT(255) NOT NULL PRIMARY KEY,"
                + "`book_Name` VARCHAR(255),"
                + "`book_Author` VARCHAR(255)"
                + ")";
        String strQueryStudentList = "CREATE TABLE `StudentList`("
                + "`student_ID` INT(255) NOT NULL PRIMARY KEY,"
                + "`student_LName` VARCHAR(255),"
                + "`student_FName` VARCHAR(255),"
                + "`student_Username` VARCHAR(255),"
                + "`student_Password` VARCHAR(255)"
                + ")";
        String strDatabaseUsername = "root";
        String strDatabasePassword = "";
        try{
            conMain = DriverManager.getConnection(strURL, strDatabaseUsername, strDatabasePassword);
            pstmtMain = conMain.prepareStatement(strQueryBookList);
            pstmtMain.execute();
            pstmtMain = conMain.prepareStatement(strQueryStudentList);
            pstmtMain.execute();
            connectDatabase();
        }
        catch(SQLException error){
            System.out.println(error);
        }
    }
    
