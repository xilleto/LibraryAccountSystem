    public int getID(String strUsername, String strPassword){
        connectDatabase();
        String strQuery = "SELECT student_ID, student_Username, student_Password FROM studentlist ORDER BY student_ID";
        try{
            pstmtMain = conMain.prepareStatement(strQuery);
            rsMain = pstmtMain.executeQuery();
            while(rsMain.next()){
                if(rsMain.getString("student_Username").equals(strUsername) && rsMain.getString("student_Password").equals(strPassword)){
                    return Integer.parseInt(rsMain.getString("student_ID"));
                }
            }
            if(tfldUsername.getText().equals("ADMIN")&&pfldPassword.getText().equals("ADMIN")){
                return 1;
            }
        }
        catch(SQLException error){
            createDatabase();
            System.out.println(error);
        }
        return 0;
    }

